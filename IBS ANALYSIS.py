# Create a copy to avoid modifying the original df_pss directly for this operation
# and ensure only the relevant columns are processed.
df_pss_corr = df_pss[list(ibs_vars.keys()) + ['PSS_Total']].copy()

# Define ordinal mappings for IBS variables
pain_severity_mapping = {
    'No pain(0%)': 0,
    'Mild': 1,
    'Moderate': 2,
    'Severe': 3,
    'Very severe (100%)': 4
}
distension_severity_mapping = {
    'No distension (0%)': 0,
    'Mild': 1,
    'Moderate': 2,
    'Severe': 3,
    'Very severe': 4 # Assuming this means 100%
}
bowel_satisfaction_mapping = {
    'Not at all satisfied (0%)': 0,
    'Somewhat satisfied': 1,
    'Moderately satisfied': 2,
    'Very satisfied (100%)': 3
}
dailylife_interference_mapping = {
    'Not at all': 0,
    'Mild': 1,
    'Moderate': 2,
    'Severe': 3,
    '100% completely interferes': 4
}

# Apply the mappings to the respective columns in df_pss_corr
df_pss_corr['Pain_Severity'] = df_pss_corr['Pain_Severity'].map(pain_severity_mapping)
df_pss_corr['Distension_Severity'] = df_pss_corr['Distension_Severity'].map(distension_severity_mapping)
df_pss_corr['Bowel_Satisfaction'] = df_pss_corr['Bowel_Satisfaction'].map(bowel_satisfaction_mapping)
df_pss_corr['DailyLife_Interference'] = df_pss_corr['DailyLife_Interference'].map(dailylife_interference_mapping)

# Columns to include in the correlation matrix (now all should be numeric)
correlation_cols = list(ibs_vars.keys()) + ['PSS_Total']

# Calculate the Spearman correlation matrix
