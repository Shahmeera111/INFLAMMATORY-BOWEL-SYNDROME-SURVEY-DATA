# Function to create frequency and percentage table
def freq_table(df, column):
    table = (
        df[column]
        .value_counts(dropna=False)
        .rename_axis('Category')
        .reset_index(name='Frequency')
    )

    table['Percentage (%)'] = (
        table['Frequency'] / len(df) * 100
    ).round(1)

    table.insert(0, 'Variable', column)

    return table

