import pandas as pd
from scipy.stats import chi2_contingency

# -----------------------------
# Detect stressed question automatically
# -----------------------------
stress_col = [c for c in df.columns if "stressed" in c.lower()][0]

# PSS Items (10)
pss_cols = [
    'PSS_UpsetUnexpectedly',
    'PSS_LackofControl',
    stress_col,
    'PSS_Confident',
    'PSS_Thingsgoingwell',
    'PSS_CouldNotCope',
    'PSS_ControlIrritations',
    'PSS_OnTopOfThings',
    'PSS_Angered',
    'PSS_Overwhelmed'
]

# Reverse-scored items
reverse_cols = [
    'PSS_Confident',
    'PSS_Thingsgoingwell',
    'PSS_ControlIrritations',
    'PSS_OnTopOfThings'
]

df_pss = df.copy()

# Reverse scoring
df_pss[reverse_cols] = 4 - df_pss[reverse_cols]

# Total PSS
df_pss["PSS_Total"] = df_pss[pss_cols].sum(axis=1)

# Stress Categories
df_pss["Stress_Level"] = pd.cut(
    df_pss["PSS_Total"],
    bins=[-1,13,26,40],
    labels=["Low","Moderate","High"]
)

# -----------------------------
# Chi-square Analysis
# -----------------------------
ibs_vars = {
    "Pain_Severity":"Pain Severity",
    "Distension_Severity":"Distension Severity",
    "Bowel_Satisfaction":"Bowel Satisfaction",
    "DailyLife_Interference":"Daily Life Interference"
}

results=[]

for col,label in ibs_vars.items():

    temp=df_pss[["Stress_Level",col]].dropna()

    contingency=pd.crosstab(
        temp["Stress_Level"],
        temp[col]
    )

    chi2,p,dof,expected=chi2_contingency(contingency)

    cells_lt5=(expected<5).sum()
    total_cells=expected.size

    results.append({
        "IBS Variable":label,
        "N":len(temp),
        "Chi-square":round(chi2,2),
        "df":dof,
        "P-value":round(p,4),
        "% Expected <5":round(cells_lt5/total_cells*100,1),
        "Significant":"Yes" if p<0.05 else "No"
    })

table4=pd.DataFrame(results)

print(table4)

display(table4)

table4.to_excel("Table4_ChiSquare.xlsx",index=False)

